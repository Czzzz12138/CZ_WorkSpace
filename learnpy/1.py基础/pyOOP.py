# 面向对象编程（Object-Oriented Programming，OOP）是一种编程范式，它将数据和操作数据的方法封装在一起，形成一个对象。
# 面向对象编程的三大特性：封装、继承、多态
# 面向对象编程的四大基本特征：抽象、封装、继承、多态
# 面向对象编程的五大基本原则：单一职责原则、开闭原则、里氏替换原则、依赖倒置原则、接口隔离原则
# 面向对象编程的六大基本概念：类、对象、属性、方法、继承、多态

